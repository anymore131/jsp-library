package com.sen;

public class SenData {
    private String sen = null;
    private int sen_num = 0;
    private int sen_right = 0;

    public String getSen() {
        return sen;
    }

    public void setSen(String sen) {
        this.sen = sen;
    }

    public int getSen_num() {
        return sen_num;
    }

    public void setSen_num(int sen_num) {
        this.sen_num = sen_num;
    }

    public int getSen_right() {
        return sen_right;
    }

    public void setSen_right(int sen_right) {
        this.sen_right = sen_right;
    }
}
